package com.github.bearboy.spring.validation;

import javax.validation.constraints.NotNull;

public class Person {

    @NotNull
    private int age;
    private String name;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
